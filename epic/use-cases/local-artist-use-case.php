	<h2>Local Artist Use Case and Interaction Flow</h2>
		<p>Maggie has always an appreciation for art, especially large outdoor installations. As an art lover, she wants to expose her two elementary-aged children to art an an early age. She is also a part-time teacher and has the idea of having her students explore Downtown Albuquerque's public art as a field trip.</p>
		<h2>Interaction Flow:</h2>
		<ol>
			<li>User loads the site on her mobile device</li>
			<li>Website displays the map of Albuquerque with all of the art locations</li>
			<li>User clicks the "login" box to sign in to her account</li>
			<li>Website verifies credentials and signs user in</li>
			<li>User clicks the "recommended routes" that are populated on the site </li>
			<li>Website displays the various route options</li>
			<li>User chooses route of her liking</li>
			<li>Website displays preferred route</li>
			<li>User walks route and bookmarks favorite sites to return to again in the future</li>
			<li>Website saves bookmarks under user's login</li>
			<li>User prompts the system to log out</li>
			<li>Website securely logs user out </li>
			<li>User returns to interface again in future</li>
		</ol>
